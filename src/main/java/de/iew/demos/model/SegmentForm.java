package de.iew.demos.model;

/**
 * Implements a form to transfer segment coordinates of a polygon between the web and service layer.
 *
 * @author Manuel Schulze <manuel_schulze@i-entwicklung.de>
 * @since 07.02.13 - 23:45
 */
public class SegmentForm {
    private double x;

    private double y;

    public SegmentForm() {
    }

    public SegmentForm(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
